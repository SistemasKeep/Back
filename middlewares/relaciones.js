    async getRelaciones(){
        const relacionesValidadas = []
        for(let relacion of this.parametros){
            if(this.relacionesValidas.includes(relacion.trim())){
                relacionesValidadas.push(relacion.trim())
            }
        }
        const relacionesModels = []
        if(!relacionesValidadas.includes("usuario_registro")){
            relacionesValidadas.push("usuario_registro")
        }
        for(const relacion of relacionesValidadas){
            let relaciones = relacion.trim().split('.');
            const main = relaciones[0]
            relaciones.shift();
            let rel;
            let haveIt = false
            if(relacionesModels.length > 0){
                for(const relModels of relacionesModels){
                    if(relModels.as !== main){
                        rel = { model: this.models[this.relacionesModel[main]], as: main }
                        if(this.relacionesModel[main] == 'usuarios'){
                            rel.attributes = { exclude: ['password','code_pass', 'key_str', 'uuid'] }
                        }else if(this.relacionesModel[main] == 'datos_facturacion'){
                            rel.attributes = { exclude: ['cer','key','password'] }
                        }else if(this.relacionesModel[main] == 'cfdis'){
                            rel.attributes = { exclude: ['xml','cadena_original'] }
                        }
                    }else{
                        rel = relModels
                        haveIt = true
                    }
                }
            }else{
                rel = { model: this.models[this.relacionesModel[main]], as: main }
                if(this.relacionesModel[main] == 'usuarios'){
                    rel.attributes = { exclude: ['password','code_pass', 'key_str', 'uuid'] }
                }else if(this.relacionesModel[main] == 'datos_facturacion'){
                    rel.attributes = { exclude: ['cer','key','password'] }
                }else if(this.relacionesModel[main] == 'cfdis'){
                    rel.attributes = { exclude: ['xml','cadena_original'] }
                }
            }
            if(!haveIt){
                rel = await this.agregarP(rel,relaciones)
                relacionesModels.push(rel)
            }else if(rel.include !== undefined){
                rel.include = await this.subAgregar(rel.include,relaciones)
            }
        }
        return relacionesModels
    }

    async agregarP(rel,relaciones) {
        const subMain = relaciones[0]
        if(subMain !== undefined){
            relaciones.shift();
            rel.include = { model: this.models[this.relacionesModel[subMain]], as: subMain }
            if(this.relacionesModel[subMain] == 'usuarios'){
                rel.include.attributes = { exclude: ['password','code_pass', 'key_str'] }
            }else if(this.relacionesModel[subMain] == 'datos_facturacion'){
                rel.include.attributes = { exclude: ['cer','key','password'] }
            }else if(this.relacionesModel[subMain] == 'cfdis'){
                rel.include.attributes = { exclude: ['xml','cadena_original'] }
            }
    
            if(relaciones.length > 0){
                rel.include = await this.agregarP(rel.include,relaciones) 
            }
        }
        return rel
    }

    async subAgregar(rel,relaciones) {
        const subMain = relaciones[0]
        if(subMain !== undefined){
            relaciones.shift();
            if(!Array.isArray(rel)){
                const relAux = rel
                rel = []
                rel.push(relAux)
                let newRel = { model: this.models[this.relacionesModel[subMain]], as: subMain }
                if(this.relacionesModel[subMain] == 'usuarios'){
                    newRel.attributes = { exclude: ['password','code_pass', 'key_str', 'uuid'] }
                }else if(this.relacionesModel[subMain] == 'datos_facturacion'){
                    newRel.attributes = { exclude: ['cer','key','password'] }
                }else if(this.relacionesModel[subMain] == 'cfdis'){
                    newRel.attributes = { exclude: ['xml','cadena_original'] }
                }
                newRel = await this.agregarP(newRel,relaciones)
                rel.push(newRel)
            }else{
                let haveIt = false
                for(const include of rel){
                    if(include.as === subMain){
                        haveIt = true
                    }
                }
                if(!haveIt){
                    let newRel = { model: this.models[this.relacionesModel[subMain]], as: subMain }
                    if(this.relacionesModel[subMain] == 'usuarios'){
                        newRel.attributes = { exclude: ['password','code_pass', 'key_str', 'uuid'] }
                    }else if(this.relacionesModel[subMain] == 'datos_facturacion'){
                        newRel.attributes = { exclude: ['cer','key','password'] }
                    }else if(this.relacionesModel[subMain] == 'cfdis'){
                        newRel.attributes = { exclude: ['xml','cadena_original'] }
                    }
                    newRel = await this.agregarP(newRel,relaciones)
                    rel.push(newRel)
                }
            }
        }
        if(relaciones.length > 0){
            let indice = rel.findIndex(objeto => objeto.as === subMain);
            rel[indice].include = await this.subAgregar(rel[indice].include,relaciones)
        }
        return rel
    }
    
