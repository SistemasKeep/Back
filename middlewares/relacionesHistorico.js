    relaciones = []
    models = []
    registro = {}
    campoBusquedas = ''
    constructor(relaciones,models,registro,campoBusquedas = undefined) {
        this.relaciones = relaciones
        this.models = models
        this.registro = registro
        this.campoBusquedas = campoBusquedas
    }
    async getRelaciones(){
       
        for (let index = 0; index < this.relaciones.length; index++) {
            const relacion = this.relaciones[index];
            if( this.relacionesModel[relacion] != 'detalle_certificados'){
                if( this.relacionesModel[relacion] != 'usuarios'){
                    const model = this.models[this.relacionesModel[relacion]];
                    const associations = model.associations;
                    const hasManyAssociations = Object.values(associations).filter(association => association.associationType !== 'HasMany');
                    this.registro[relacion] = await this.models[this.relacionesModel[relacion]].findByPk(this.registro["id_" + relacion], {include: hasManyAssociations})
                }else{
                    this.registro[relacion] = await this.models[this.relacionesModel[relacion]].findByPk(this.registro["id_" + relacion],{ attributes: { exclude: ['password','code_pass', 'uuid'] } })
                    const marcas = []
                    const registrosEncontrados = await this.models.marcas_usuarios.findAll({
                        where: {
                            id_usuario: this.registro.id,
                            deletedAt: null
                        },
                        include: ['marca']
                    });
                    for (let index = 0; index < registrosEncontrados.length; index++) {
                        const reg = registrosEncontrados[index];
                        marcas.push(reg.marca)
                    }
                    this.registro.marcas = marcas
                }
                if(this.registro[relacion] != null){
                    try {
                        this.registro[relacion].usuario_registro.password = undefined
                        this.registro[relacion].usuario_registro.code_pass = undefined
                    } catch (error) {
                        
                    }
                }
            }
        }
        return this.registro
    }
    async getRelacionesBelongTo(){
        if(this.campoBusquedas !== undefined){
            for (let index = 0; index < this.relaciones.length; index++) {
                let whereFind = {where:{}}
                whereFind.where[this.campoBusquedas[index]] = this.registro.id
                const relacion = this.relaciones[index];
                if(relacion == "roles_usuarios"){
                    const model = this.models[this.relacionesModel[relacion]];
                    const associations = model.associations;
                    const hasManyAssociations = Object.values(associations).filter(association => association.associationType !== 'HasMany');
                    whereFind.include = hasManyAssociations
                    let relacionesEncontradas = await this.models[relacion].findAll(whereFind)
                    const roles = []
                    const permisos = []
                    const permisosAux = []
                    for (let index = 0; index < relacionesEncontradas.length; index++) {
                        const roleUsuario = relacionesEncontradas[index];
                        if(roleUsuario.rol != null){
                            const model = this.models.roles;
                            const associations = model.associations;
                            const hasManyAssociations = Object.values(associations).filter(association => association.associationType !== 'HasMany');
                            const rol = await this.models.roles.findByPk(roleUsuario.rol.id);
                            const rolAux = await this.models.roles.findByPk(roleUsuario.rol.id,{
                                include: hasManyAssociations
                            });
                            for (let j = 0; j < rolAux.permisos.length; j++) {
                                const permiso = rolAux.permisos[j];
                                const permisoFull = await this.models.permisos.findByPk(permiso.id_permiso);
                                if(permisoFull != null){
                                    if(!permisosAux.includes(permisoFull.display_name)){
                                        permisosAux.push(permisoFull.display_name)
                                        permisos.push(permisoFull)
                                    }

                                }
                            }


                            rol.permisos = undefined
                            roles.push(rol)
                        }
                    }
                    this.registro['roles'] = roles
                    this.registro['permisos'] = permisos
                } else if( relacion == 'usuarios'){
                    let relacionesEncontradas = await this.models[relacion].findAll(whereFind)
                    this.registro[relacion] = relacionesEncontradas[0]
                }
            }
        }
        return this.registro
    }
